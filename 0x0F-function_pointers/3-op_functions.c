#include "3-calc.h"

/**
 * op_add - A function that returns the addition of two numbers
 * @a: First number
 * @b: Second number
 * Return: Addtion of two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - A function that returns the subtraction of two numbers
 * @a: First number
 * @b: Second number
 * Return: Subtraction of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - A function that returns the multiplication of two numbers
 * @a: First number
 * @b: Second number
 * Return: Multiplication of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - A function that returns the division of two numbers
 * @a: First number
 * @b: Second number
 * Return: Diviosion of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - A function that returns the modulus of two numbers
 * @a: First number
 * @b: Second number
 * Return: Return modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
